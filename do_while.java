public class do_while {
    public static void main(String[] args) {
        // Initialize the counter to start from 1 (the first natural number)
        int i = 1;

        // Using a do-while loop to print natural numbers
        do {
            System.out.println(i);
            i++;
        } while (i <= 100);  // Continue the loop until i exceeds 100
    }

}
